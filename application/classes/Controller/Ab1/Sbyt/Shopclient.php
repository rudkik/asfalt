    }

    /**
     * Задаем клиентам статус покупатель по реализации и штучному товару
     */
    public function action_set_is_buyer()
    {
        $this->_sitePageData->url = '/sbyt/shopclient/set_is_buyer';

        $params = Request_RequestParams::setParams(
            array(
                'group_by' => array(
                    'shop_client_id'
                )
            )
        );

        // реализация
        $ids = Request_Request::findBranch('DB_Ab1_Shop_Car',
            array(), $this->_sitePageData, $this->_driverDB, $params
        );
        if(count($ids->childs) > 0){
            $this->_driverDB->sendSQL('UPDATE ab_shop_clients SET is_buyer = 1 WHERE id in ('.implode(',', $ids->getChildArrayInt('shop_client_id', true)).')');
        }

        // штучный товар
        $ids = Request_Request::findBranch('DB_Ab1_Shop_Piece',
            array(), $this->_sitePageData, $this->_driverDB, $params
        );
        if(count($ids->childs) > 0){
            $this->_driverDB->sendSQL('UPDATE ab_shop_clients SET is_buyer = 1 WHERE id in ('.implode(',', $ids->getChildArrayInt('shop_client_id', true)).')');
        }

        $this->redirect('/sbyt/shopclient/index');
    }

    public function action_invoice()
    {
        $this->_sitePageData->url = '/sbyt/shopclient/invoice';

        // задаем данные, которые будут меняться
        $this->_setGlobalDatas(
            array(
                'view::_shop/client/one/invoice',
            )
        );

        // id записи
        $shopClientID = Request_RequestParams::getParamInt('shop_client_id');
        $model = new Model_Ab1_Shop_Client();
        if (! $this->dublicateObjectLanguage($model, $shopClientID, $this->_sitePageData->shopMainID, FALSE)) {
            throw new HTTP_Exception_404('Client not is found!');
        }


        $data = new MyArray();
        $data->setValues($model, $this->_sitePageData);

        $params = Request_RequestParams::setParams(
            array(
                'date_from_equally' => Request_RequestParams::getParamDate('date_from_equally'),
                'date_to' => Request_RequestParams::getParamDate('date_to'),
                'shop_client_id' => $shopClientID,
                'empty_date_give_to_client' => true,
                'count_id' => true,
            )
        );

        $ids = Request_Request::find('DB_Ab1_Shop_Act_Service',
            $this->_sitePageData->shopID, $this->_sitePageData, $this->_driverDB,
            $params, 0, TRUE
        );
        $data->additionDatas['act_services'] = $ids->childs[0]->values['count'];

        $ids = Request_Request::find('DB_Ab1_Shop_Invoice',
            $this->_sitePageData->shopID, $this->_sitePageData, $this->_driverDB,
            $params, 0, TRUE
        );
        $data->additionDatas['invoices'] = $ids->childs[0]->values['count'];


        // получаем данные
       // $data = View_View::findOne('DB_Ab1_Shop_Client', $this->_sitePageData->shopMainID, "_shop/client/one/invoice",
      //      $this->_sitePageData, $this->_driverDB, array('id' => $shopClientID), array());



        $data = Helpers_View::getViewObject(
            $data, $model, "_shop/client/one/invoice", $this->_sitePageData, $this->_driverDB,
            $this->_sitePageData->shopMainID
        );

        $this->response->body($data);
    }

