    /**
     * Загружаем список актов
     */
    public function action_load_acts()
    {
        $this->_sitePageData->url = '/sbyt/shopclient/load_acts';

        $filePath = DOCROOT . '1S'.DIRECTORY_SEPARATOR. 'acts' . DIRECTORY_SEPARATOR . 'load'.DIRECTORY_SEPARATOR;

        $files = glob($filePath . '*.xml');

        // сортируем файлы по дате изменения
        $list = array();
        foreach ($files as $file) {
            try {
                $list[date('Y-m-d H:i:s', filemtime($file)) . '_' . $file] = $file;
            }catch (Exception $e){}
        }
        ksort($list);
        $files = $list;

        foreach ($files as $file) {
            try {
                $info = pathinfo($file);
                $new = $info['dirname'].DIRECTORY_SEPARATOR.'tmp'.DIRECTORY_SEPARATOR.$info['filename'].'_'.random_int(1,100000).'.txt';
                rename($file, $new);
                Api_Ab1_Shop_Act_Revise::loadXML($new, $this->_sitePageData, $this->_driverDB);
                //unlink($new);
            }catch (Exception $e){}
        }
        echo 'finish';
    }
